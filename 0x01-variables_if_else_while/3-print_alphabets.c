#include <stdlib.h>
/**
 * main - Entry point 
 *
 * Return: Always 0 (success)
 */
int main(void)
{

	char alphabet;

	char alphabet2;

	alphabet = 'a';
	alphabet2 = 'A';
	while
	(alphabet <= 'z') {
		putchar(alphabet);
		alphabet++;
	}
	while
	(alphabet <= 'Z') {
		putchar(alphabet2);
		alphabet2++;
	}
	putchar('\n');
	return (0);
}
